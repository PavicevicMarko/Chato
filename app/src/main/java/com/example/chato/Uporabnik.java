package com.example.chato;

public class Uporabnik {

    private String ime, fonska;

    public Uporabnik (String ime, String fonska){
        this.ime = ime;
        this.fonska = fonska;
    }

    public String getIme() {
        return ime;
    }

    public String getFonska() {
        return fonska;
    }


}
